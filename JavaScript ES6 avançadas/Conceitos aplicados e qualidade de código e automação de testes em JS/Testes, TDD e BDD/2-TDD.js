/* 
    # TDD (Test Driven Development) - Pt-br (Desenvolvimento Orientado a Testes)
    
        É um dos pilares do Extreme Programing, consiste em testar e refatorar em pequenos ciclos,
    onde você escreve o teste antes do código, faz o mesmo passar e refator o código

    3 etapas: Escrita do teste -> Escrita do código -> Refatoração. E o cliclo se repete ao longo do desenvolvimento.

    ### Vantagens

    * Feedback rápido;
    * Maior segurança em alterações e novas funcionalidades;
    * Código mais limpo;
    * Produtividade.
*/