/*
    Um operador de atribuição atribui um valor ao operando à sua esquerda baseado no valor
    do operando à direita. O operador de atribuição básico é o igual (=),
    que atribui o valor do operando à direita ao operando à esquerda.
*/

// Atribuição
x = y

// Atribuição de adição
x = x + y // ou
x += y

// Atribuição de subtração
x = x - y //ou
x -= y

// Atribuição de multiplicação
x = x * y // ou
x *= y

// Atribuição de divisão
x = x / y // ou
x /= y

// Atribuição de resto
x = x % y // ou
x %= y